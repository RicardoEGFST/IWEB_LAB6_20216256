package org.example.lab6_20216256.beans;


public class Peliculas {
    private int idPelicula;
    private String tituloPeli;
    private String directorPeli;
    private  int anioPeli;
    private double ratingPeli;
    private double boxOfficePeli;
    private String generoPeli;

    public int getIdPelicula() {
        return idPelicula;
    }

    public void setIdPelicula(int idPelicula) {
        this.idPelicula = idPelicula;
    }

    public String getTituloPeli() {
        return tituloPeli;
    }

    public void setTituloPeli(String tituloPeli) {
        this.tituloPeli = tituloPeli;
    }

    public String getDirectorPeli() {
        return directorPeli;
    }

    public void setDirectorPeli(String directorPeli) {
        this.directorPeli = directorPeli;
    }

    public int getAnioPeli() {
        return anioPeli;
    }

    public void setAnioPeli(int anioPeli) {
        this.anioPeli = anioPeli;
    }

    public double getRatingPeli() {
        return ratingPeli;
    }

    public void setRatingPeli(double ratingPeli) {
        this.ratingPeli = ratingPeli;
    }

    public double getBoxOfficePeli() {
        return boxOfficePeli;
    }

    public void setBoxOfficePeli(double boxOfficePeli) {
        this.boxOfficePeli = boxOfficePeli;
    }

    public String getGeneroPeli() {
        return generoPeli;
    }

    public void setGeneroPeli(String generoPeli) {
        this.generoPeli = generoPeli;
    }
}
